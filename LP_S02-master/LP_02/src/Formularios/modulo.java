package Formularios;

public class modulo {
	
	public int sumar(int x, int y){
		int suma = x+y;
		return suma;
	}
	public int resta(int x, int y){
		int resta=x-y;
		return resta;
	}
	public int multi(int x, int y){
		int multi= x*y;
		return multi;
	}
	public int divi(int x, int y){
		int divi = x/y;
		return divi;
	}
}
